#include "ioCC2530.h"
#include "LED.h"

/*****************************************************************************
*函数含义：不精确延迟函数
*函数参数：延迟毫秒数
*  返回值：无返回值
*****************************************************************************/
void delayMS(int ms)
{
  int i,j;
  for(i = 0; i < ms; i++)
  {
    for(j = 0; j < 535; j++);
  }
}


void main()
{
  ledInit();    //初始化LED
  while(1)      
  {
    led1On();
    delayMS(500);
    led1Off();
    delayMS(500);
  }
}